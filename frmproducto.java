    private void btmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmodificarActionPerformed
dtm.setValueAt(txnombre.getText().trim(), filaSeleccionada, 0);
        dtm.setValueAt(txdescripcion.getText().trim(), filaSeleccionada, 1);
        dtm.setValueAt(txprecio.getText().trim(), filaSeleccionada, 2);
        dtm.setValueAt(txstock.getText().trim(), filaSeleccionada, 3);
        dtm.setValueAt(txcategoria.getText().trim(), filaSeleccionada, 4);

       txnombre.setText("");
        txdescripcion.setText("");
        txprecio.setText("");
        txstock.setText("");
        txcategoria.setText("");
        txnombre.requestFocus();
        
        btguardar.setEnabled(true);
        btcargar.setEnabled(true);
        bteliminar.setEnabled(true);
        btmodificar.setEnabled(false);




















        // TODO add your handling code here:
    }//GEN-LAST:event_btmodificarActionPerformed

    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
        datosFila[0] = txnombre.getText().trim();
        datosFila[1] = txdescripcion.getText().trim();
        datosFila[2] = txprecio.getText().trim();
        datosFila[3] = txstock.getText().trim();
        datosFila[4] = txcategoria.getText().trim();
        dtm.addRow(datosFila);
        
        txnombre.setText("");
        txdescripcion.setText("");
        txprecio.setText("");
        txstock.setText("");
        txcategoria.setText("");
        
        txnombre.requestFocus();
        
        btcargar.setEnabled(true);
        bteliminar.setEnabled(true);




        // TODO add your handling code here:
    }//GEN-LAST:event_btguardarActionPerformed

    private void btcargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcargarActionPerformed
       if(tablacontenido.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null, "No ha seleccionado registro en la tabla", "ERRROR AL MODIFICAR REGISTRO", JOptionPane.WARNING_MESSAGE);
        }else{
        filaSeleccionada = tablacontenido.getSelectedRow();
        txnombre.setText(dtm.getValueAt(tablacontenido.getSelectedRow(), 0).toString());
        txdescripcion.setText(dtm.getValueAt(tablacontenido.getSelectedRow(), 1).toString());
        txprecio.setText(dtm.getValueAt(tablacontenido.getSelectedRow(), 2).toString());
        txstock.setText(dtm.getValueAt(tablacontenido.getSelectedRow(), 3).toString());
        txcategoria.setText(dtm.getValueAt(tablacontenido.getSelectedRow(), 4).toString());
        txnombre.requestFocus();
        
        btguardar.setEnabled(false);
        btcargar.setEnabled(false);
        bteliminar.setEnabled(false);
        btmodificar.setEnabled(true);
        }
        







// TODO add your handling code here:
    }//GEN-LAST:event_btcargarActionPerformed

    private void bteliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteliminarActionPerformed

if(tablacontenido.getSelectedRow()==-1){
        JOptionPane.showMessageDialog(null, "No ha seleccionado registro en la tabla", "ERRROR ELIMINAR REGISTRO", JOptionPane.ERROR_MESSAGE);
        }else{
        JOptionPane.showConfirmDialog(null, "Desea eliminar este registro ?", "CONFIRMACION DE ELIMINACION", JOptionPane.YES_NO_OPTION);
        dtm.removeRow(tablacontenido.getSelectedRow());
        }











        // TODO add your handling code here:
    }//GEN-LAST:event_bteliminarActionPerformed

